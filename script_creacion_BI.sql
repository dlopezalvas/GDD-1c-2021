/* Precio promedio de PCs, vendidos y comprados */
CREATE VIEW [BREAKFAST_CLUB].BI_PRECIO_PROMEDIO_PC AS
SELECT T.COD_PC, (SELECT ROUND(CAST(AVG(PRECIO) AS decimal(18,1)),1) 
FROM [BREAKFAST_CLUB].BI_HECHOS_PC C WHERE TIPO_TRANSACCION = 'Compra' AND C.COD_PC = T.COD_PC) AS PRECIO_PROMEDIO_COMPRA, 
(SELECT ROUND(CAST(AVG(PRECIO) AS decimal(18,1)),1) 
FROM [BREAKFAST_CLUB].BI_HECHOS_PC V WHERE TIPO_TRANSACCION = 'Venta' AND V.COD_PC = T.COD_PC) AS PRECIO_PROMEDIO_VENTA
FROM [BREAKFAST_CLUB].BI_HECHOS_PC T GROUP BY T.COD_PC
GO

/* Cantidad de PCs, vendidos y comprados x sucursal y mes */
CREATE VIEW [BREAKFAST_CLUB].BI_CANT_COMPRAS_VENTAS_PC_X_SUCURSAL_X_MES AS
SELECT T.COD_PC, T.COD_SUCURSAL, MES, ANIO , ISNULL((SELECT SUM(CANTIDAD)  
FROM [BREAKFAST_CLUB].BI_HECHOS_PC C WHERE TIPO_TRANSACCION = 'Compra' 
AND C.COD_PC = T.COD_PC AND C.COD_SUCURSAL = T.COD_SUCURSAL AND C.COD_TIEMPO = T.COD_TIEMPO),0) AS CANTIDAD_COMPRAS, 
ISNULL((SELECT SUM(CANTIDAD) FROM [BREAKFAST_CLUB].BI_HECHOS_PC C WHERE TIPO_TRANSACCION = 'Venta' 
AND C.COD_PC = T.COD_PC AND C.COD_SUCURSAL = T.COD_SUCURSAL AND C.COD_TIEMPO = T.COD_TIEMPO),0) AS CANTIDAD_VENTAS 
FROM [BREAKFAST_CLUB].BI_HECHOS_PC T JOIN [BREAKFAST_CLUB].BI_TIEMPO TI ON T.COD_TIEMPO = TI.COD_TIEMPO GROUP BY T.COD_PC, T.COD_SUCURSAL, MES, ANIO, T.COD_TIEMPO
GO
 
/* Ganancias (precio de venta – precio de compra) x Sucursal x mes */
CREATE VIEW [BREAKFAST_CLUB].BI_GANANCIAS_PC_X_SUCURSAL_X_MES AS
SELECT T.COD_SUCURSAL, MES, ANIO, ISNULL((SELECT SUM(ISNULL(PRECIO,0) * ISNULL(CANTIDAD,0)) FROM [BREAKFAST_CLUB].BI_HECHOS_PC V 
WHERE TIPO_TRANSACCION = 'Venta'  AND V.COD_TIEMPO = T.COD_TIEMPO AND V.COD_SUCURSAL = T.COD_SUCURSAL GROUP BY V.COD_TIEMPO),0) - ISNULL((SELECT SUM(ISNULL(PRECIO,0) * ISNULL(CANTIDAD,0)) 
FROM [BREAKFAST_CLUB].BI_HECHOS_PC C WHERE TIPO_TRANSACCION = 'Compra' AND C.COD_TIEMPO = T.COD_TIEMPO AND C.COD_SUCURSAL = T.COD_SUCURSAL
GROUP BY C.COD_TIEMPO),0) AS GANANCIA 
FROM [BREAKFAST_CLUB].BI_HECHOS_PC T JOIN [BREAKFAST_CLUB].BI_TIEMPO TI ON T.COD_TIEMPO = TI.COD_TIEMPO 
GROUP BY T.COD_SUCURSAL, MES, ANIO, T.COD_TIEMPO
GO

/* VISTAS ACCESORIOS */
/* Precio promedio de cada accesorio, vendido y comprado */
CREATE VIEW [BREAKFAST_CLUB].BI_PRECIO_PROMEDIO_ACCESORIO AS
SELECT T.COD_ACCESORIO, (SELECT ROUND(CAST(AVG(PRECIO) AS decimal(18,1)),1) 
FROM [BREAKFAST_CLUB].BI_HECHOS_ACCESORIOS C WHERE TIPO_TRANSACCION = 'Compra' AND C.COD_ACCESORIO = T.COD_ACCESORIO) AS PRECIO_PROMEDIO_COMPRA, 
(SELECT ROUND(CAST(AVG(PRECIO) AS decimal(18,1)),1) 
FROM [BREAKFAST_CLUB].BI_HECHOS_ACCESORIOS V WHERE TIPO_TRANSACCION = 'Venta' AND V.COD_ACCESORIO = T.COD_ACCESORIO) AS PRECIO_PROMEDIO_VENTA
FROM [BREAKFAST_CLUB].BI_HECHOS_ACCESORIOS T GROUP BY T.COD_ACCESORIO
GO

/* Ganancias (precio de venta – precio de compra) x Sucursal x mes */
CREATE VIEW [BREAKFAST_CLUB].BI_GANANCIAS_ACCESORIO_X_SUCURSAL_X_MES AS
SELECT T.COD_SUCURSAL, MES, ANIO, 
ISNULL((SELECT SUM(ISNULL(PRECIO,0) * ISNULL(CANTIDAD,0)) FROM [BREAKFAST_CLUB].BI_HECHOS_ACCESORIOS V 
WHERE TIPO_TRANSACCION = 'Venta'  AND V.COD_TIEMPO = T.COD_TIEMPO AND V.COD_SUCURSAL = T.COD_SUCURSAL GROUP BY V.COD_TIEMPO),0) - 
ISNULL((SELECT SUM(ISNULL(PRECIO,0) * ISNULL(CANTIDAD,0)) FROM [BREAKFAST_CLUB].BI_HECHOS_ACCESORIOS C 
WHERE TIPO_TRANSACCION = 'Compra' AND C.COD_TIEMPO = T.COD_TIEMPO AND C.COD_SUCURSAL = T.COD_SUCURSAL GROUP BY C.COD_TIEMPO),0) AS GANANCIA 
FROM [BREAKFAST_CLUB].BI_HECHOS_ACCESORIOS T JOIN [BREAKFAST_CLUB].BI_TIEMPO TI ON T.COD_TIEMPO = TI.COD_TIEMPO 
GROUP BY T.COD_SUCURSAL, MES, ANIO, T.COD_TIEMPO
GO

/* Promedio de tiempo en stock de cada modelo de accesorio */
