CREATE FUNCTION [BREAKFAST_CLUB].DIFERENCIA_TIEMPO_ACCES_SUCURSAL(@COD_ACCESORIO DECIMAL(18,0), @COD_SUCURSAL DECIMAL(18,0))
RETURNS  DECIMAL(18,0) AS
BEGIN

	DECLARE @ventas TABLE (cant_ventas DECIMAL(18,0), tiempo DECIMAL(18,0))
	INSERT INTO @ventas	SELECT SUM(CANTIDAD), SUM(CANTIDAD * COD_TIEMPO) FROM [BREAKFAST_CLUB].BI_HECHOS_ACCESORIOS  
	WHERE TIPO_TRANSACCION = 'VENTA' AND COD_ACCESORIO = @COD_ACCESORIO AND COD_SUCURSAL = @COD_SUCURSAL

	DECLARE @CANT_TOTAL_VENTAS DECIMAL(18,0) = (select cant_ventas from @ventas)
	DECLARE @TIEMPO_VENTA DECIMAL(18,0) = (select tiempo from @ventas)

	DECLARE @MES_ANTERIOR DECIMAL(18,0) = (SELECT MAX(A.COD_TIEMPO) FROM (SELECT ha.cod_tiempo AS COD_TIEMPO FROM   [BREAKFAST_CLUB].BI_HECHOS_ACCESORIOS ha
	JOIN   [BREAKFAST_CLUB].BI_HECHOS_ACCESORIOS ha1 ON (ha1.COD_TIEMPO <= ha.cod_tiempo 
	and ha.COD_ACCESORIO = ha1.COD_ACCESORIO and ha.COD_SUCURSAL = ha1.COD_SUCURSAL and ha.TIPO_TRANSACCION = ha1.TIPO_TRANSACCION)
	WHERE  ha.CANTIDAD < @CANT_TOTAL_VENTAS and ha1.COD_ACCESORIO = @COD_ACCESORIO and ha1.COD_SUCURSAL = @COD_SUCURSAL	and ha1.TIPO_TRANSACCION = 'Compra'
	GROUP  BY ha.cod_tiempo	HAVING SUM(ha1.CANTIDAD) <  @CANT_TOTAL_VENTAS) A)

	DECLARE @compras_anteriores TABLE (cant_compras DECIMAL(18,0), tiempo_compras DECIMAL(18,0))
	INSERT INTO @compras_anteriores SELECT SUM(CANTIDAD), SUM(CANTIDAD * COD_TIEMPO) AS TOTAL FROM [BREAKFAST_CLUB].BI_HECHOS_ACCESORIOS 
	WHERE TIPO_TRANSACCION = 'COMPRA' AND COD_ACCESORIO = @COD_ACCESORIO AND COD_SUCURSAL = @COD_SUCURSAL AND COD_TIEMPO <= @MES_ANTERIOR

	DECLARE @TIEMPO_COMPRA DECIMAL(18,0) = (select tiempo_compras from @compras_anteriores)
	DECLARE @CANT_TOTAL_MES_ANTERIOR DECIMAL(18,0) = (select cant_compras from @compras_anteriores) 

	SET @TIEMPO_COMPRA = @TIEMPO_COMPRA + (@CANT_TOTAL_VENTAS - @CANT_TOTAL_MES_ANTERIOR) * (@MES_ANTERIOR + 1)
	 
	DECLARE @RETORNO DECIMAL(18,0) = @TIEMPO_VENTA - @TIEMPO_COMPRA

	RETURN @RETORNO
END
GO

CREATE FUNCTION [BREAKFAST_CLUB].TIEMPO_PROMEDIO_EN_STOCK_ACCESORIO(@COD_ACCESORIO DECIMAL(18,0))
RETURNS  DECIMAL(18,4) AS
BEGIN
	DECLARE @RETORNO DECIMAL(18,4) = (SELECT SUM([BREAKFAST_CLUB].DIFERENCIA_TIEMPO_ACCES_SUCURSAL(@COD_ACCESORIO , COD_SUCURSAL))	
									FROM [BREAKFAST_CLUB].BI_SUCURSAL)

	DECLARE @CANT_TOTAL_VENTAS DECIMAL(18,4) = (SELECT SUM(CANTIDAD) FROM [BREAKFAST_CLUB].BI_HECHOS_ACCESORIOS  
	WHERE TIPO_TRANSACCION = 'VENTA' AND COD_ACCESORIO = @COD_ACCESORIO)

	SET @RETORNO = @RETORNO/@CANT_TOTAL_VENTAS

	RETURN @RETORNO
END						
GO

